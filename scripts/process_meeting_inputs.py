"""
Mighty-Link AI Connect: Process Meeting Inputs Script (T609 - Meet/Gemini integration)
Author: Antigravity 2.0 (AI Agent)

This script parses a Meeting Minutes markdown file (generated by Meet/Gemini session,
e.g. docs/meetings/2026-06-02_CEO_Meeting_Minutes.md), automatically extracts:
1. The chosen service direction (A/B/C/D) and merges corresponding WBS Phase 7 tasks.
2. Unresolved action items (宿題/課題) and appends them to data/issues_tracker.tsv.
3. Newly raised questions/answers (質疑応答) and appends them to data/qa_tracker.tsv.

After parsing and merging, it automatically executes all Workspace Sync scripts
(Sheets, Calendar, Drive/NotebookLM docs) to establish a seamless Meeting-Driven Development loop.
"""

