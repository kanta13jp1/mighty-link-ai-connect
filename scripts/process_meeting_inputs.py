TSV_WBS = os.path.join(DATA_DIR, "WBS.tsv")
TSV_ISSUES = os.path.join(DATA_DIR, "issues_tracker.tsv")
TSV_QA = os.path.join(DATA_DIR, "qa_tracker.tsv")

DEFAULT_MEETING_MINUTES = os.path.join(MEETINGS_DIR, "2026-06-02_CEO_Meeting_Minutes.md")
SPREADSHEET_ID = "1L99HCBHr4IsVUWqnUuG6OgoUmxEQUdfaYQim1n6etB8"

def get_next_issue_id(filepath):
    """Finds the next sequential R-ID by scanning issues_tracker.tsv."""
    max_num = 27  # Default fallback based on existing R1-R27
    if os.path.exists(filepath):
        with open(filepath, "r", encoding="utf-8") as f:
            reader = csv.reader(f, delimiter="\t")
            for row in reader:
                if row and row[0].startswith("R"):
                    try:
                        num = int(row[0][1:])
                        if num > max_num:
                            max_num = num
                    except ValueError:
                        pass
    return f"R{max_num + 1}"

def get_next_qa_id(filepath):
    """Finds the next sequential QA-ID by scanning qa_tracker.tsv."""
    max_num = 22  # Default fallback based on existing QA-01 to QA-22
    if os.path.exists(filepath):
        with open(filepath, "r", encoding="utf-8") as f:
            reader = csv.reader(f, delimiter="\t")
            for row in reader:
                if row and row[0].startswith("QA-"):
                    try:
                        num = int(row[0][3:])
                        if num > max_num:
                            max_num = num
                    except ValueError:
                        pass
    return f"QA-{max_num + 1:02d}"

def parse_meeting_minutes(filepath):
    """Parses meeting minutes markdown and extracts WBS choice, issues, and QA."""
    if not os.path.exists(filepath):
        print(f"[-] Error: Meeting Minutes file not found at {filepath}")
        return None, [], []

    print(f"[*] Parsing Meeting Minutes: {os.path.relpath(filepath, PROJECT_ROOT)}")
    with open(filepath, "r", encoding="utf-8") as f:
        content = f.read()

    # 1. Parse service direction choice
    direction_choice = None
    dir_match = re.search(r"(?:決定されたサービス方向性|サービス方向性|方向性):\s*([A-Da-d])", content)
    if dir_match:
        direction_choice = dir_match.group(1).upper()
        print(f"[+] Detected service direction choice: {direction_choice}")
    else:
        print("[!] Warning: Could not detect service direction choice in the document.")

    # 2. Parse issues / action items
    issues = []
    # Seek sections like "## 保留事項・宿題"
    issues_section = re.search(r"##\s*(?:保留事項・宿題|宿題|課題|アクションアイテム).*?\n(.*?)(?=\n##|$)", content, re.DOTALL)
    if issues_section:
        lines = issues_section.group(1).strip().split("\n")
        for line in lines:
            # Matches unresolved checkbox issues: - [ ] [category][severity] Title ・ Impact ・ Mitigation (オーナー:Owner, WBS:TXXX, Docs:docs/X)
            # Highly resilient extraction
            if re.match(r"^\s*-\s*\[\s*[ ]\s*\]", line):
                pattern = r"-\s*\[\s*\]\s*\[(.*?)\]\s*\[(.*?)\]\s*(.*?)\s*・\s*(.*?)\s*・\s*(.*?)\s*\(オーナー:(.*?), WBS:(.*?), Docs:(.*?)\)"
                match = re.match(pattern, line)
                if match:
                    issues.append({
                        "category": match.group(1).strip(),
                        "severity": match.group(2).strip().upper(),
                        "title": match.group(3).strip(),
                        "impact": match.group(4).strip(),
                        "mitigation": match.group(5).strip(),
                        "owner": match.group(6).strip(),
                        "wbs": match.group(7).strip(),
                        "docs": match.group(8).strip()
                    })
                else:
                    # Fallback parser for simple manual entries
                    clean_line = re.sub(r"^\s*-\s*\[\s*[ ]\s*\]\s*", "", line).strip()
                    if clean_line:
                        issues.append({
                            "category": "adhoc-meet",
                            "severity": "MED",
                            "title": clean_line,
                            "impact": "Meet会議で挙がった宿題事項",
                            "mitigation": "担当者による後追い確認",
                            "owner": "人間",
                            "wbs": "",
                            "docs": ""
                        })

    # 3. Parse QA
    qa_list = []
    # Seek sections like "## 質疑応答" or "## Q&A"
    qa_section = re.search(r"##\s*(?:質疑応答|Q&A|想定QA).*?\n(.*?)(?=\n##|$)", content, re.DOTALL)
    if qa_section:
        text = qa_section.group(1).strip()
        # Find Q&A blocks
        q_matches = re.finditer(r"-\s*Q:\s*(.*?)\n-\s*A:\s*(.*?)(?=\n-\s*Q:|\n\n|$)", text, re.DOTALL)
        for m in q_matches:
            q_text = m.group(1).strip()
            a_block = m.group(2).strip()
            
            # Parse answer block details
            # E.g., [回答方針] ... [保留時の対応] ... [カテゴリ] ... [Docs] ...
            ans_match = re.search(r"\[回答方針\]\s*(.*?)\s*\[保留時の対応\]\s*(.*?)\s*\[カテゴリ\]\s*(.*?)\s*\[Docs\]\s*(.*)", a_block)
            if ans_match:
                qa_list.append({
                    "question": q_text,
                    "policy": ans_match.group(1).strip(),
                    "deferral": ans_match.group(2).strip(),
                    "category": ans_match.group(3).strip(),
                    "docs": ans_match.group(4).strip()
                })
            else:
                qa_list.append({
                    "question": q_text,
                    "policy": a_block,
                    "deferral": "特になし",
                    "category": "adhoc-qa",
                    "docs": ""
                })

    return direction_choice, issues, qa_list

def append_issues(issues):
    """Appends new issues safely to issues_tracker.tsv."""
    if not issues:
        return
    
    print(f"[*] Appending {len(issues)} new issues to issues_tracker.tsv...")
    today = datetime.date.today().isoformat()
    
    new_rows = []
    for issue in issues:
        next_id = get_next_issue_id(TSV_ISSUES)
        # Schema: ID, カテゴリ, 重要度, 状態, タイトル, 影響, 緩和策, オーナー, 起票日, 解決予定日, 関連 WBS, 関連 docs, 関連 Issue, メメモ, 更新日
        new_row = [
            next_id,
            issue["category"],
            issue["severity"],
            "open",
            issue["title"],
            issue["impact"],
            issue["mitigation"],
            issue["owner"],
            today,
            "",  # Target resolution date empty initially
            issue["wbs"],
            issue["docs"],
            "",  # Associated Github issue ID empty initially
            "Meet/Gemini議事録から自動起票",
            today
        ]
        new_rows.append(new_row)
        # Write to local cache so sequential IDs work
        with open(TSV_ISSUES, "a", encoding="utf-8", newline="") as f:
            writer = csv.writer(f, delimiter="\t")
            writer.writerow(new_row)
            
    print(f"[+] Appended successfully. Last ID generated: {next_id}")

def append_qa(qa_list):
    """Appends new QA pairs safely to qa_tracker.tsv."""
    if not qa_list:
        return
    
    print(f"[*] Appending {len(qa_list)} new QA pairs to qa_tracker.tsv...")
    today = datetime.date.today().isoformat()
    
    for qa in qa_list:
        next_id = get_next_qa_id(TSV_QA)
        # Schema: ID, カテゴリ, 質問, 回答方針, 保留時の対応, 関連論点, 関連 docs, 出典, 状態, 更新日
        new_row = [
            next_id,
            qa["category"],
            qa["question"],
            qa["policy"],
            qa["deferral"],
            "",  # Associated decision axis empty initially
            qa["docs"],
            "Meet 2026-06-02",
            "想定済",
            today
        ]
        with open(TSV_QA, "a", encoding="utf-8", newline="") as f:
            writer = csv.writer(f, delimiter="\t")
            writer.writerow(new_row)
            
    print(f"[+] Appended successfully. Last ID generated: {next_id}")

